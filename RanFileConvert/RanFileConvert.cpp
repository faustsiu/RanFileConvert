void help ()
{
	printf ("usage RanFileConvert [Format] [Infile] [OutFile]\n");
	printf ("[Format] 1 = Normal to Ran1, 2 = Ran1 to Normal, 3 = Ran1 to Ran2, 4 = Ran2 to Ran1\n");
}


int _tmain(int argc, _TCHAR* argv[])
{
	FILE *fi, *fo;
	int c, f, o = 0, cnt = 0;

    if (argc < 4 || argc > 5)
	{
		help ();
	    return 1;
	}

	if (argc == 5)
	{
		o = _wtoi (argv[4]);
	}

    printf ("%s %s %s", (char *)argv[1], (char *)argv[2], (char *)argv[3]);

	fi = _wfopen (argv[2], L"rb");
	fo = _wfopen (argv[3], L"wb");

	if (fi != NULL && fo != NULL)
	{
		f = *(char *)argv[1] - '1';
        if (f < 0 || f > 3)
			f = 0;

    	while (1)
	    {
			c = fgetc (fi);

			if (feof(fi))
				break;

			if (cnt++ >= o)
			    fputc(Coding[f][c], fo);
			else
			    fputc(c, fo);
    	}
	}
	
    fclose (fi);
	fclose (fo);

	return 0;
}

